/***********************************************************************
 *
 * 　入力情報を保持するクラス.
 *
***********************************************************************/

class InputMessage
{
    #key;   // string:   キーの名前.
    #state; // KeyState: キーの状態.


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    * 引数:
    *   string   key:   キーの名前.
    *   KeyState state: キーの状態.
    *
    +-----------------------------------------------------------------*/
    constructor(key, state)
    {
        this.#key = key;
        this.#state = state;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: キーの名前を取得する.
    *
    * 戻り値:
    *   string: キーの名前.
    *
    +-----------------------------------------------------------------*/
    GetKey()
    {
        return this.#key;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: キーの状態を取得する.
    *
    * 戻り値:
    *   string: キーの状態.
    *
    +-----------------------------------------------------------------*/
    GetState()
    {
        return this.#state;
    }
}