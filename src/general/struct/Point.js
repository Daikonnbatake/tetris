/***********************************************************************
 *
 *   2次元の座標を表すクラス.
 *
***********************************************************************/

class Point
{
    #x;  // number: X座標
    #y;  // number: Y座標


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    * 引数:
    *   number x: X座標.
    *   number y: Y座標.
    *
    +-----------------------------------------------------------------*/
    constructor(x, y)
    {
        this.#x = x;
        this.#y = y;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: 全てのフィールドが 0 の Point 構造体を取得する.
    *
    * 戻り値:
    *   Point: 全フィールドが 0 の Point 構造体.
    *
    +-----------------------------------------------------------------*/
    static Default()
    {
        return new Point(0, 0);
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: X座標を取得する.
    *
    * 戻り値:
    *   number: X座標.
    *
    +-----------------------------------------------------------------*/
    GetX()
    {
        return this.#x;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: Y座標を取得する.
    *
    * 戻り値:
    *   number: Y座標.
    *
    +-----------------------------------------------------------------*/
    GetY()
    {
        return this.#y;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: X座標を設定する.
    *
    * 引数:
    *   number x: X座標.
    *
    +-----------------------------------------------------------------*/
    SetX(x)
    {
        this.#x = x;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: Y座標を設定する.
    *
    * 引数:
    *   number y: Y座標.
    *
    +-----------------------------------------------------------------*/
    SetY(y)
    {
        this.#y = y;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: この座標と他の座標との差を求める.
    *
    * 引数:
    *   Point: point 比較対象の座標.
    *
    +-----------------------------------------------------------------*/
    Difference(point)
    {
        const targetX = point.#x;
        const targetY = point.#y;
        const thisX   = this.#x;
        const thisY   = this.#y;

        return new Point(targetX - thisX, targetY - thisY);
    }
}