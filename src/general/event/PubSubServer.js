/***********************************************************************
 *
 * 　クラス間のメッセージ交換を補助するクラス.
 *
***********************************************************************/

class PubSubServer
{
    #channels; // Object<string, Channel>: チャンネルの辞書.


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    +-----------------------------------------------------------------*/
    constructor()
    {
        this.#channels = {};
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: チャンネルを作成する.
    *
    * 引数:
    *   string  name:  チャンネル名.
    *
    +-----------------------------------------------------------------*/
    CreateChannel(name)
    {
        this.#channels[name] = new Channel();
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: チャンネルを削除する.
    *
    * 引数:
    *   string  name:  チャンネル名.
    *
    +-----------------------------------------------------------------*/
    DeleteChannel(name)
    {
        this.#channels.delete(name);
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: チャンネルを取得する.
    *
    * 引数:
    *   string  name:  チャンネル名.
    *
    +-----------------------------------------------------------------*/
    GetChannel(name)
    {
        return this.#channels[name];
    }
}