/***********************************************************************
 *
 *   レベルをカウントするクラス.
 *
***********************************************************************/

class LevelCounter
{
    #level; // number: レベル.


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    +-----------------------------------------------------------------*/
    constructor()
    {
        this.#level = 1;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: レベルを位置段階上げる.
    *
    +-----------------------------------------------------------------*/
    LevelUp()
    {
        this.#level += 1;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: 現在のレベルを取得する.
    *
    * 戻り値:
    *   number: 現在のレベル.
    *
    +-----------------------------------------------------------------*/
    GetNowLevel()
    {
        return this.#level;
    }
}