/***********************************************************************
 *
 *   ブロックの情報を保持するクラス.
 *
***********************************************************************/

class Block
{
    #type;   // number: このブロックの種類.
    #hidden; // bool:   このブロックが非表示なら true.


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    * 引数:
    *   number blockType: ブロックの種類.
    *   number hidden:    可視性の設定(true の時非表示).
    *
    +-----------------------------------------------------------------*/
    constructor(blockType, hidden)
    {
        this.#type   = blockType;
        this.#hidden = hidden;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: ブロックの種類を変更する.
    *
    * 引数:
    *   number blockType: ブロックの種類.
    *
    +-----------------------------------------------------------------*/
    ChangeType(blockType)
    {
        this.#type = blockType;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: このブロックを非表示にする.
    *
    +-----------------------------------------------------------------*/
    Hide()
    {
        this.#hidden = true;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: このブロックを表示する.
    *
    +-----------------------------------------------------------------*/
    Show()
    {
        this.#hidden = false;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: このブロックの種類を取得する.
    *
    * 戻り値:
    *   number: このブロックの種類.
    *
    +-----------------------------------------------------------------*/
    GetType()
    {
        return this.#type;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: このブロックが非表示かどうかを取得する.
    *
    * 戻り値:
    *   bool: このブロックが非表示なら true を返す.
    *
    +-----------------------------------------------------------------*/
    IsHidden()
    {
        return this.#hidden;
    }
}