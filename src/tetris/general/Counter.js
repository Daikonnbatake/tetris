/***********************************************************************
 *
 *   汎用的な数量カウンター.
 *
***********************************************************************/

class Counter
{
    #counter; // number: カウンター.


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    +-----------------------------------------------------------------*/
    constructor()
    {
        this.#counter = 0;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: カウントアップする.
    *
    * 引数:
    *   number vavlue: カウントする数(未指定の場合1カウントする).
    *
    +-----------------------------------------------------------------*/
    CountUp(value = 1)
    {
        this.#counter += value;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: カウンターをリセット.
    *
    +-----------------------------------------------------------------*/
    Reset()
    {
        this.#counter = 0;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: 現在の値を取得する.
    *
    * 戻り値:
    *   number: 現在の値.
    *
    +-----------------------------------------------------------------*/
    GetValue()
    {
        return this.#counter;
    }
}