/***********************************************************************
 *
 * 　2次元の拡大率を表す構造体.
 *
***********************************************************************/

class Scale
{
    #x; // number: X軸の拡大率
    #y; // number: Y軸の拡大率


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    * 引数:
    *   number  X:  X軸の拡大率.
    *   number  Y:  Y軸の拡大率.
    *
    +-----------------------------------------------------------------*/
    constructor(x, y)
    {
        this.#x = x;
        this.#y = y;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: X軸の拡大率を取得する.
    *
    * 戻り値:
    *   number: X軸の拡大率.
    *
    +-----------------------------------------------------------------*/
    X()
    {
        return this.#x;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: Y軸の拡大率を取得する.
    *
    * 戻り値:
    *   number: Y軸の拡大率.
    *
    +-----------------------------------------------------------------*/
    Y()
    {
        return this.#y;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: X軸の拡大率を設定する.
    *
    * 引数:
    *   number: X軸の拡大率.
    *
    +-----------------------------------------------------------------*/
    X(x)
    {
        this.#x = x;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: Y軸の拡大率を設定する.
    *
    * 引数:
    *   number: Y軸の拡大率.
    *
    +-----------------------------------------------------------------*/
    Y(y)
    {
        this.#y = y;
    }
}