/***********************************************************************
 *
 * 　2次元の座標を表す構造体.
 *
***********************************************************************/

class Point
{
    #x;  // number: X座標
    #y;  // number: Y座標


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    * 引数:
    *   number  x:  X座標.
    *   number  y:  Y座標.
    *
    +-----------------------------------------------------------------*/
    constructor(x, y)
    {
        this.#x = x;
        this.#y = y;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: X座標を取得する.
    *
    * 戻り値:
    *   number:  X座標.
    *
    +-----------------------------------------------------------------*/
    X()
    {
        return this.#x;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: Y座標を取得する.
    *
    * 戻り値:
    *   number:  Y座標.
    *
    +-----------------------------------------------------------------*/
    Y()
    {
        return this.#y;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: X座標を設定する.
    *
    * 引数:
    *   number:  X座標.
    *
    +-----------------------------------------------------------------*/
    X(x)
    {
        this.#x = x;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: Y座標を設定する.
    *
    * 引数:
    *   number:  Y座標.
    *
    +-----------------------------------------------------------------*/
    Y(y)
    {
        this.#y = y;
    }
}