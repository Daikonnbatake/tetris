/***********************************************************************
 *
 * 　2次元の大きさを表す構造体.
 *
***********************************************************************/

class Size
{
    #width;  // number: 横幅
    #height; // number: 高さ


    /*-----------------------------------------------------------------+
    *
    * 説明: コンストラクタ.
    *
    * 引数:
    *   number  width:   横幅.
    *   number  height:  高さ.
    *
    +-----------------------------------------------------------------*/
    constructor(width, height)
    {
        this.#width = width;
        this.#height = height;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: 横幅を取得する.
    *
    * 戻り値:
    *   number: 横幅.
    *
    +-----------------------------------------------------------------*/
    Width()
    {
        return this.#width;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: 高さを取得する.
    *
    * 戻り値:
    *   number: 高さ.
    *
    +-----------------------------------------------------------------*/
    Height()
    {
        return this.#height;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: 横幅を設定する.
    *
    * 引数:
    *   number: 横幅.
    *
    +-----------------------------------------------------------------*/
    Width(width)
    {
        this.#width = width;
    }


    /*-----------------------------------------------------------------+
    *
    * 説明: 高さを設定する.
    *
    * 引数:
    *   number: 高さ.
    *
    +-----------------------------------------------------------------*/
    Height(height)
    {
        this.#height = height;
    }
}